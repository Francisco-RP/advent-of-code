Deno.test("part 1 example small", async () => {
  const testInput = `
########
#..O.O.#
##@.O..#
#...O..#
#.#.O..#
#...O..#
#......#
########

<^^>>>vv<v>>v<<
`.trim();
  assertStrictEquals(await part1(testInput), 2028);
});

