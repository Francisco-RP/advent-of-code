Deno.test("part 2 example small", async () => {
  const testInput = `
#######
#...#.#
#.....#
#..OO@#
#..O..#
#.....#
#######

<vv<<^^<<^^
`.trim();
  /*
ends at
##############
##...[].##..## <-- 100*1 + 5 = 105
##...@.[]...## <-- 100*2 + 7 = 207
##....[]....## <-- 100*3 + 6 = 306
##..........##
##..........##
##############
*/
  assertStrictEquals(await part2(testInput), 105 + 207 + 306);
});

